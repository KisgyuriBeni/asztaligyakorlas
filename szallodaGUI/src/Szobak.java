public class Szobak {
    int id;
    int szoba_merete;
    int agyak_szama;
    int terasz;
    int haziallat;
    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getSzoba_merete() {
        return szoba_merete;
    }
    public void setSzoba_merete(int szoba_merete) {
        this.szoba_merete = szoba_merete;
    }
    public int getAgyak_szama() {
        return agyak_szama;
    }
    public void setAgyak_szama(int agyak_szama) {
        this.agyak_szama = agyak_szama;
    }
    public int getTerasz() {
        return terasz;
    }
    public void setTerasz(int terasz) {
        this.terasz = terasz;
    }
    public int getHaziallat() {
        return haziallat;
    }
    public void setHaziallat(int haziallat) {
        this.haziallat = haziallat;
    }

    
}
