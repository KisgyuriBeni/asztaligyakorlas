public class Foglalok {
    int id;
    String nev;
    int szemelyek;
    String foglalasok_eleje;
    String foglalasok_vege;
    int szoba_id;
    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getNev() {
        return nev;
    }
    public void setNev(String nev) {
        this.nev = nev;
    }
    public int getSzemelyek() {
        return szemelyek;
    }
    public void setSzemelyek(int szemelyek) {
        this.szemelyek = szemelyek;
    }
    public String getFoglalasok_eleje() {
        return foglalasok_eleje;
    }
    public void setFoglalasok_eleje(String foglalasok_eleje) {
        this.foglalasok_eleje = foglalasok_eleje;
    }
    public String getFoglalasok_vege() {
        return foglalasok_vege;
    }
    public void setFoglalasok_vege(String foglalasok_vege) {
        this.foglalasok_vege = foglalasok_vege;
    }
    public int getSzoba_id() {
        return szoba_id;
    }
    public void setSzoba_id(int szoba_id) {
        this.szoba_id = szoba_id;
    }

    
}
